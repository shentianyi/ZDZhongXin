package com.zd.csms.marketing.contants;

/**
 * 支付方式
 * @author licheng
 *
 */
public enum PayModeContant {
	/**
	 * 付费方式：1：月  2：季度 3：半年 4：年
	 */
	PAYMODE_MONTH(1,"月"),
	
	/**
	 * 付费方式：1：月  2：季度 3：半年 4：年
	 */
	PAYMODE_QUARTER(2,"季度"),
	
	/**
	 * 付费方式：1：月  2：季度 3：半年 4：年
	 */
	PAYMODE_SIXMONTH(3,"半年"),
	
	/**
	 * 付费方式：1：月  2：季度 3：半年 4：年
	 */
	PAYMODE_YEAR(4,"年");
	
	
	
	private int code;
	private String name;
	public int getCode() {
		return code;
	}
	public String getName() {
		return name;
	}
	private PayModeContant(int code, String name) {
		this.code = code;
		this.name = name;
	}
	
}
