package com.zd.csms.marketing.model;

import java.util.Date;

import com.zd.core.annotation.table;

/**
 * 市场部审批
 * @author licheng
 *
 */
@table(name="market_approval")
public class MarketApprovalVO {
	private int id;
	private int approvalType;//审批类型
	private String marketopinion;//审批意见
	private String approvalPerson;//审批人
	private String remark;//备注
	private int approvalObjectId;//审批目标对象Id
	private Date createDate;//审批时间
	private int approvalUserRole;//审批人角色
	private int ApprovalResult;//审批结果 1：同意 2：不同意
	private int approvalUserId;//审批人Id
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getApprovalType() {
		return approvalType;
	}
	public void setApprovalType(int approvalType) {
		this.approvalType = approvalType;
	}
	public String getMarketopinion() {
		return marketopinion;
	}
	public void setMarketopinion(String marketopinion) {
		this.marketopinion = marketopinion;
	}
	public String getApprovalPerson() {
		return approvalPerson;
	}
	public void setApprovalPerson(String approvalPerson) {
		this.approvalPerson = approvalPerson;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}

	public MarketApprovalVO() {
		super();
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public int getApprovalObjectId() {
		return approvalObjectId;
	}
	public void setApprovalObjectId(int approvalObjectId) {
		this.approvalObjectId = approvalObjectId;
	}
	public int getApprovalResult() {
		return ApprovalResult;
	}
	public void setApprovalResult(int approvalResult) {
		ApprovalResult = approvalResult;
	}
	public int getApprovalUserId() {
		return approvalUserId;
	}
	public void setApprovalUserId(int approvalUserId) {
		this.approvalUserId = approvalUserId;
	}
	public int getApprovalUserRole() {
		return approvalUserRole;
	}
	public void setApprovalUserRole(int approvalUserRole) {
		this.approvalUserRole = approvalUserRole;
	}
	
	
}
