package com.zd.csms.marketing.model;

import java.util.Date;

public class BusinessTransferQueryVO {
	private int id;
	private Integer pageType;//1：待审批 2：已审批 
	private Integer currRole;//当前角色
	private String dealerName;
	private String remark;
	private int approvalState;
	private int currUserId;
	
	private Date createDateStart;
	private Date createDateEnd;
	private String bankName;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Integer getCurrRole() {
		return currRole;
	}
	public void setCurrRole(Integer currRole) {
		this.currRole = currRole;
	}
	public Integer getPageType() {
		return pageType;
	}
	public void setPageType(Integer pageType) {
		this.pageType = pageType;
	}
	public String getDealerName() {
		return dealerName;
	}
	public void setDealerName(String dealerName) {
		this.dealerName = dealerName;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public int getApprovalState() {
		return approvalState;
	}
	public void setApprovalState(int approvalState) {
		this.approvalState = approvalState;
	}
	public int getCurrUserId() {
		return currUserId;
	}
	public void setCurrUserId(int currUserId) {
		this.currUserId = currUserId;
	}
	public Date getCreateDateStart() {
		return createDateStart;
	}
	public void setCreateDateStart(Date createDateStart) {
		this.createDateStart = createDateStart;
	}
	public Date getCreateDateEnd() {
		return createDateEnd;
	}
	public void setCreateDateEnd(Date createDateEnd) {
		this.createDateEnd = createDateEnd;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}

	
}
