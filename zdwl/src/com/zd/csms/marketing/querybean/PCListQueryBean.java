package com.zd.csms.marketing.querybean;

import java.util.Date;

/**
 * 进驻流转列表查询Bean
 * @author licheng
 *
 */
public class PCListQueryBean {
	private int id;
	private String dealerName;//经销商名称
	private String approvalState;//审批状态
	private String bankName;//金融机构
	private Integer nextApproval;
	
	private String createUserName;
	private Date createDate;
	private String lastModifyUserName;
	private Date lastModifyDate;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDealerName() {
		return dealerName;
	}
	public void setDealerName(String dealerName) {
		this.dealerName = dealerName;
	}
	public String getApprovalState() {
		return approvalState;
	}
	public void setApprovalState(String approvalState) {
		this.approvalState = approvalState;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getCreateUserName() {
		return createUserName;
	}
	public void setCreateUserName(String createUserName) {
		this.createUserName = createUserName;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public String getLastModifyUserName() {
		return lastModifyUserName;
	}
	public void setLastModifyUserName(String lastModifyUserName) {
		this.lastModifyUserName = lastModifyUserName;
	}
	public Date getLastModifyDate() {
		return lastModifyDate;
	}
	public void setLastModifyDate(Date lastModifyDate) {
		this.lastModifyDate = lastModifyDate;
	}
	public Integer getNextApproval() {
		return nextApproval;
	}
	public void setNextApproval(Integer nextApproval) {
		this.nextApproval = nextApproval;
	}

	
}
