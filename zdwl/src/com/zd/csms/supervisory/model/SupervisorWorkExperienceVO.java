package com.zd.csms.supervisory.model;

import java.io.Serializable;
import java.util.Date;

import com.zd.core.annotation.table;
@table(name="T_SUPERVISOR_WORKEXPERIENCE")
public class SupervisorWorkExperienceVO implements Serializable {
	
	private static final long serialVersionUID = 6348857295726046280L;
	/**
	 * 主键ID
	 */
	private int id;                
	/**
	 * 起(工作经历)
	 */
	private Date  workStartTime;     
	/**
	 * 止(工作经历)
	 */
	private Date  workEndTime;       
	/**
	 * 服务机构
	 */
	private String serviceOrganization;
	/**
	 * 职位
	 */
	private String position;           
	/**
	 * 薪资
	 */
	private String compensation;    
	/**
	 * 离职原因
	 */
	private String leaveReason;      
	/**
	 * 证明人
	 */
	private String certifier;       
	/**
	 * 联系方式
	 */
	private String contactNumber;   
	/**
	 * 监管员ID
	 */
	private int supervisorId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getCompensation() {
		return compensation;
	}
	public void setCompensation(String compensation) {
		this.compensation = compensation;
	}
	public Date getWorkStartTime() {
		return workStartTime;
	}
	public void setWorkStartTime(Date workStartTime) {
		this.workStartTime = workStartTime;
	}
	public Date getWorkEndTime() {
		return workEndTime;
	}
	public void setWorkEndTime(Date workEndTime) {
		this.workEndTime = workEndTime;
	}
	public String getServiceOrganization() {
		return serviceOrganization;
	}
	public void setServiceOrganization(String serviceOrganization) {
		this.serviceOrganization = serviceOrganization;
	}
	public String getLeaveReason() {
		return leaveReason;
	}
	public void setLeaveReason(String leaveReason) {
		this.leaveReason = leaveReason;
	}
	public String getCertifier() {
		return certifier;
	}
	public void setCertifier(String certifier) {
		this.certifier = certifier;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public int getSupervisorId() {
		return supervisorId;
	}
	public void setSupervisorId(int supervisorId) {
		this.supervisorId = supervisorId;
	}
	
}
