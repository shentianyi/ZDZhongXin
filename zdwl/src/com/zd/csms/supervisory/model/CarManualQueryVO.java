package com.zd.csms.supervisory.model;

public class CarManualQueryVO {

	private int id;
	private int sid;//车辆id
	private int userid;//交付人
	private int receiveid;//接收人
	private String draft_num;//票号
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public int getReceiveid() {
		return receiveid;
	}
	public void setReceiveid(int receiveid) {
		this.receiveid = receiveid;
	}
	public String getDraft_num() {
		return draft_num;
	}
	public void setDraft_num(String draftNum) {
		draft_num = draftNum;
	}
	
	
}
