package com.zd.csms.supervisory.querybean;

public class CheckStockDealerQueryBean {
	private int id;
	private int dealerId;//经销商主键ID
	private int checkStockId;//查库频次主键ID
	private String dealerName;//经销商名称
	private String bankName;//银行名称
	private String brand;//品牌
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getDealerId() {
		return dealerId;
	}
	public void setDealerId(int dealerId) {
		this.dealerId = dealerId;
	}
	public int getCheckStockId() {
		return checkStockId;
	}
	public void setCheckStockId(int checkStockId) {
		this.checkStockId = checkStockId;
	}
	public String getDealerName() {
		return dealerName;
	}
	public void setDealerName(String dealerName) {
		this.dealerName = dealerName;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	
}
