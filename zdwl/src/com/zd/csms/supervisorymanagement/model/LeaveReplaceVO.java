package com.zd.csms.supervisorymanagement.model;

import java.io.Serializable;
import java.util.Date;

import com.zd.core.annotation.table;
/**
 * 请假替岗人
 * @author macongcong
 *
 */
@table(name="t_leave_replace")
public class LeaveReplaceVO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8674048326295642192L;
	/**
	 * 主键ID
	 */
	private int id;
	/**
	 * 请假申请ID
	 */
	private int leaveApplyId;
	/**
	 * 替岗监管员储备库
	 */
	private int replaceSupervisory;
	/**
	 * 替岗开始时间
	 */
	private Date replaceStartTime;
	/**
	 * 替岗结束时间
	 */
	private Date replaceEndTime;
	/**
	 * 替岗天数
	 */
	private double replaceDays;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getLeaveApplyId() {
		return leaveApplyId;
	}
	public void setLeaveApplyId(int leaveApplyId) {
		this.leaveApplyId = leaveApplyId;
	}
	public int getReplaceSupervisory() {
		return replaceSupervisory;
	}
	public void setReplaceSupervisory(int replaceSupervisory) {
		this.replaceSupervisory = replaceSupervisory;
	}
	public Date getReplaceStartTime() {
		return replaceStartTime;
	}
	public void setReplaceStartTime(Date replaceStartTime) {
		this.replaceStartTime = replaceStartTime;
	}
	public Date getReplaceEndTime() {
		return replaceEndTime;
	}
	public void setReplaceEndTime(Date replaceEndTime) {
		this.replaceEndTime = replaceEndTime;
	}
	public double getReplaceDays() {
		return replaceDays;
	}
	public void setReplaceDays(double replaceDays) {
		this.replaceDays = replaceDays;
	}
	
}
