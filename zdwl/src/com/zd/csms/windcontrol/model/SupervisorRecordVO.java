package com.zd.csms.windcontrol.model;

//检查过程中监管员优/缺点
public class SupervisorRecordVO {
	private int inspectionId;// 巡店报告id
	private String supervisorContent;// 描述问题
	private String supervisorResult;// 处理结果

	public int getInspectionId() {
		return inspectionId;
	}

	public void setInspectionId(int inspectionId) {
		this.inspectionId = inspectionId;
	}

	public String getSupervisorContent() {
		return supervisorContent;
	}

	public void setSupervisorContent(String supervisorContent) {
		this.supervisorContent = supervisorContent;
	}

	public String getSupervisorResult() {
		return supervisorResult;
	}

	public void setSupervisorResult(String supervisorResult) {
		this.supervisorResult = supervisorResult;
	}

}
