package com.zd.csms.rbac.web.form;

import org.apache.struts.action.ActionForm;

import com.zd.csms.rbac.model.RoleQueryVO;
import com.zd.csms.rbac.model.RoleVO;

public class RoleForm extends ActionForm {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8688214511944313836L;
	private RoleVO role = new RoleVO();					//角色操作对象（用于增删改操作）
	private RoleQueryVO roleQuery = new RoleQueryVO();	//角色查询对象（用于列表查询）
	private int[] roleIds;								//角色id数组（用于列表操作）
	public RoleVO getRole() {
		return role;
	}
	public void setRole(RoleVO role) {
		this.role = role;
	}
	public RoleQueryVO getRoleQuery() {
		return roleQuery;
	}
	public void setRoleQuery(RoleQueryVO roleQuery) {
		this.roleQuery = roleQuery;
	}
	public int[] getRoleIds() {
		return roleIds;
	}
	public void setRoleIds(int[] roleIds) {
		this.roleIds = roleIds;
	}
	
	
}
