import java.io.Serializable;
import java.util.List;

import com.zd.csms.rbac.model.ResourceVO;

public class MenuNodeVO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7380367551919489724L;
	
	private ResourceVO node;			//资源对象
	private List<MenuNodeVO> childNode;	//下级菜单节点
	public ResourceVO getNode() {
		return node;
	}
	public void setNode(ResourceVO node) {
		this.node = node;
	}
	public List<MenuNodeVO> getChildNode() {
		return childNode;
	}
	public void setChildNode(List<MenuNodeVO> childNode) {
		this.childNode = childNode;
	}
	

}
