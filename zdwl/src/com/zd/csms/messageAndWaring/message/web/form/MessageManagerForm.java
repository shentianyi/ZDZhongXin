package com.zd.csms.messageAndWaring.message.web.form;

import org.apache.struts.action.ActionForm;
/**
 * 信息提醒表单
 * 
 * @author zhangzhicheng
 *
 */
public class MessageManagerForm extends ActionForm {
	private static final long serialVersionUID = 3345696458037132632L;
	private int msgType;// 消息分类（进驻7天未交监管费提醒，监管费到期前30天进行提醒）
	private int userId;// 用户id
	private String idsRead;//已读
	private String idsShield; // 屏蔽

	public int getMsgType() {
		return msgType;
	}

	public void setMsgType(int msgType) {
		this.msgType = msgType;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getIdsRead() {
		return idsRead;
	}

	public void setIdsRead(String idsRead) {
		this.idsRead = idsRead;
	}

	public String getIdsShield() {
		return idsShield;
	}

	public void setIdsShield(String idsShield) {
		this.idsShield = idsShield;
	}
    	
	
}